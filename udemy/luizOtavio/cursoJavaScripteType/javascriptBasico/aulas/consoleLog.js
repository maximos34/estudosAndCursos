/*
console.log é um metodo de console que coloca no console de desenvolvimento alguma informação .
qualquer texto precisa ser escrito entre aspas simples ou duplas mais podemos usar as crases sendo chamado de template strings .
vc pode usar aspas simples ou duplas numa string somente se vc usar a outra opção entre elas por exemplo vc pode usar aspas simples num texto se vc por aspas duplas entre eles
*/

console.log('olá Mundo'); // aspas simples
console.log("olá Mundo"); // aspas duplas 
console.log(`'pedro'"jose"`); // usando crases para template string

